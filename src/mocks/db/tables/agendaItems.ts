/**
 * Get all agenda items for a meeting
 */
export const getAgendaItemsByMeetingId = (meetingId: string): AgendaItemRow[] => {
  return agendaItemsTable.filter((item) => item.meetingId === meetingId);
};

/**
 * Get all agenda items for an agenda
 */
export const getAgendaItemsByAgendaId = (agendaId: string): AgendaItemRow[] => {
  return agendaItemsTable.filter((item) => item.agendaId === agendaId);
};

/**
 * Get a specific agenda item by ID
 */
export const getAgendaItemById = (itemId: string): AgendaItemRow | undefined => {
  return agendaItemsTable.find((item) => item.id === itemId);
};

/**
 * Get root-level agenda items (no parent) for a meeting
 */
export const getRootAgendaItems = (meetingId: string): AgendaItemRow[] => {
  return agendaItemsTable
    .filter((item) => item.meetingId === meetingId && item.parentItemId === null)
    .sort((a, b) => a.orderIndex - b.orderIndex);
};

/**
 * Get sub-items of a parent agenda item
 */
export const getSubItems = (parentItemId: string): AgendaItemRow[] => {
  return agendaItemsTable
    .filter((item) => item.parentItemId === parentItemId)
    .sort((a, b) => a.orderIndex - b.orderIndex);
};

/**
 * Calculate total estimated duration for an agenda
 */
export const calculateTotalDuration = (agendaId: string): number => {
  return agendaItemsTable
    .filter((item) => item.agendaId === agendaId)
    .reduce((total, item) => total + item.estimatedDuration, 0);
};

/**
 * Get items by status for a meeting
 */
export const getItemsByStatus = (meetingId: string, status: AgendaItemStatus): AgendaItemRow[] => {
  return agendaItemsTable.filter((item) => item.meetingId === meetingId && item.status === status);
};

/**
 * Get completed items for a meeting
 */
export const getCompletedItems = (meetingId: string): AgendaItemRow[] => {
  return getItemsByStatus(meetingId, 'completed');
};

/**
 * Get pending items for a meeting
 */
export const getPendingItems = (meetingId: string): AgendaItemRow[] => {
  return getItemsByStatus(meetingId, 'pending');
};

/**
 * Get in-progress items for a meeting
 */
export const getInProgressItems = (meetingId: string): AgendaItemRow[] => {
  return getItemsByStatus(meetingId, 'in_progress');
};
