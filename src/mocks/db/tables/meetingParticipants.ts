/**
 * Get all participants for a meeting
 */
export const getParticipantsByMeetingId = (meetingId: string): MeetingParticipantRow[] => {
  return meetingParticipantsTable.filter((p) => p.meetingId === meetingId);
};

/**
 * Get a specific participant
 */
export const getParticipantById = (id: string): MeetingParticipantRow | undefined => {
  return meetingParticipantsTable.find((p) => p.id === id);
};

/**
 * Get participants by user ID (all meetings a user is part of)
 */
export const getParticipantsByUserId = (userId: number): MeetingParticipantRow[] => {
  return meetingParticipantsTable.filter((p) => p.userId === userId);
};

/**
 * Get voting-eligible participants for a meeting
 */
export const getVotingEligibleParticipants = (meetingId: string): MeetingParticipantRow[] => {
  return meetingParticipantsTable.filter((p) => p.meetingId === meetingId && p.canVote);
};

/**
 * Get required participants for quorum calculation
 */
export const getRequiredParticipants = (meetingId: string): MeetingParticipantRow[] => {
  return meetingParticipantsTable.filter((p) => p.meetingId === meetingId && p.isRequired);
};

/**
 * Get attendance counts for a meeting
 */
export const getAttendanceCounts = (
  meetingId: string
): {
  present: number;
  absent: number;
  late: number;
  left_early: number;
  excused: number;
  total: number;
} => {
  const participants = getParticipantsByMeetingId(meetingId);
  return {
    present: participants.filter((p) => p.attendanceStatus === 'present').length,
    absent: participants.filter((p) => p.attendanceStatus === 'absent').length,
    late: participants.filter((p) => p.attendanceStatus === 'late').length,
    left_early: participants.filter((p) => p.attendanceStatus === 'left_early').length,
    excused: participants.filter((p) => p.attendanceStatus === 'excused').length,
    total: participants.length,
  };
};

/**
 * Get RSVP summary for a meeting
 */
export const getRSVPSummary = (
  meetingId: string
): {
  accepted: number;
  declined: number;
  pending: number;
  tentative: number;
  total: number;
} => {
  const participants = getParticipantsByMeetingId(meetingId);
  return {
    accepted: participants.filter((p) => p.rsvpStatus === 'accepted').length,
    declined: participants.filter((p) => p.rsvpStatus === 'declined').length,
    pending: participants.filter((p) => p.rsvpStatus === 'pending').length,
    tentative: participants.filter((p) => p.rsvpStatus === 'tentative').length,
    total: participants.length,
  };
};

/**
 * Check if a user is a participant in a meeting
 */
export const isParticipant = (meetingId: string, userId: number): boolean => {
  return meetingParticipantsTable.some((p) => p.meetingId === meetingId && p.userId === userId);
};

/**
 * Check if a user can vote in a meeting
 */
export const canUserVote = (meetingId: string, userId: number): boolean => {
  const participant = meetingParticipantsTable.find(
    (p) => p.meetingId === meetingId && p.userId === userId
  );
  return participant?.canVote ?? false;
};
