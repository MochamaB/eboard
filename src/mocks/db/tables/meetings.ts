/**
 * Meetings Table - Flat database-like structure
 * Stores meeting records across all boards
 */

export type MeetingType = 'regular' | 'special' | 'emergency' | 'annual' | 'committee';
export type LocationType = 'physical' | 'virtual' | 'hybrid';
export type MeetingStatus =
  | 'draft'
  | 'pending_confirmation'
  | 'confirmed'
  | 'scheduled'
  | 'in_progress'
  | 'completed'
  | 'cancelled'
  | 'rejected';

export interface MeetingRow {
  id: string;
  boardId: string;

  // Basic info
  title: string;
  description: string;
  meetingType: MeetingType;

  // Scheduling
  scheduledDate: string; // ISO date
  startTime: string; // HH:mm format
  endTime: string; // HH:mm format
  duration: number; // minutes
  timezone: string;

  // Location
  locationType: LocationType;
  physicalLocation: string | null;
  meetingLink: string | null;

  // Workflow status
  status: MeetingStatus;

  // Confirmation workflow
  confirmationRequired: boolean;
  confirmedBy: number | null; // userId
  confirmedAt: string | null;
  confirmationDocumentId: string | null; // FK to documents (polymorphic)
  rejectionReason: string | null;

  // Quorum settings
  quorumPercentage: number;
  quorumRequired: number; // calculated absolute number

  // Metadata
  createdBy: number; // userId
  createdAt: string;
  updatedAt: string;
  cancelledBy: number | null;
  cancelledAt: string | null;
  cancellationReason: string | null;
}

